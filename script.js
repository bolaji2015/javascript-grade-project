
let displaymath = document.getElementById('display math')
// let math = document.getElementById('math')
function grade() {
    if (math.value >= 70 && 100){
        displaymath.innerHTML = "A Excellent";
    }else
    if(math.value >= 60 && 69) {
        displaymath.innerHTML = "B very good";
    }else
    if(math.value >= 50 && 59) {
        displaymath.innerHTML = "C good";
    }else
    if(math.value >= 40 && 49) { 
        displaymath.innerHTML = "D pass";
    }else
    if(math.value <= 40) {
        displaymath.innerHTML = "F fail"
    }
}

